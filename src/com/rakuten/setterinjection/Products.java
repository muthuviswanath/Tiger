package com.rakuten.setterinjection;

public class Products {

	private int pid;
	private String pname;
	private int qty;
	private double price;
	public Products(int pid, String pname, int qty, double price) {
		this.pid = pid;
		this.pname = pname;
		this.qty = qty;
		this.price = price;
	}
	
	
	
}
